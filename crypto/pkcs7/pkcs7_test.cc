  EXPECT_TRUE(PKCS7_SIGNER_INFO_set(p7si, ecdsa_x509.get(), ecdsa_pkey.get(),
                                    EVP_sha256()));
  EXPECT_TRUE(PKCS7_add_signer(p7.get(), p7si));

